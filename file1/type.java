public class type{
    public static void main(String k[]){
        int a = 5;
        float b = a; // type conversion
        System.out.println(b);

        //type casting
        float c = 24.16f;
        int d = (int)c;
        System.out.println(d);

        char ch = 'a';
        int number = ch;
        System.out.println(number);
    }
}